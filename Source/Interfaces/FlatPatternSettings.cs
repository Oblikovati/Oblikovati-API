namespace Oblikovati.API;

/// <summary>
/// Provides access to flat pattern related document settings.
/// </summary>
public interface FlatPatternSettings
{
    /// <summary>
    /// Gets the root Application object. Where the property is weakly-typed, it can be set to (QueryInterfaced for) 'Application' when running with Inventor whereas, 'ApprenticeServer' when running with the Apprentice Server.
    /// </summary>
    object Application { get; }
    /// <summary>
    /// Gets the parent object from whom this object can logically be reached.
    /// </summary>
    _Document Parent { get; }
    /// <summary>
    /// Gets the constant that indicates the type of this object.
    /// </summary>
    ObjectTypeEnum Type { get; }
    /// <summary>
    /// Read-write property that gets and sets Flat Pattern defer update status.
    /// </summary>
    bool DeferUpdate { get; set; }
}
